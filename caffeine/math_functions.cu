template <typename Dtype>
void caffe_gpu_copy(int N, const Dtype* X, Dtype* Y) {
    if (X != Y) {
        CUDA_CHECK(cudaMemcpy(Y, X, sizeof(Dtype) * N, cudaMemcpyDefault));
    }
}

